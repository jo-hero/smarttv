package net.jo.model;

public class VideoRelateInfo {
    private String name;
    private String actor;
    private String detail;
    private String score;
    private String region;
    private String category;
    private String length;
    private String last_updatetime;
    private String release_time;
    private boolean is_movie;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getActor() {
        return actor;
    }

    public void setActor(String actor) {
        this.actor = actor;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {
        this.length = length;
    }

    public String getLast_updatetime() {
        return last_updatetime;
    }

    public void setLast_updatetime(String last_updatetime) {
        this.last_updatetime = last_updatetime;
    }

    public String getRelease_time() {
        return release_time;
    }

    public void setRelease_time(String release_time) {
        this.release_time = release_time;
    }

    public boolean isIs_movie() {
        return is_movie;
    }

    public void setIs_movie(boolean is_movie) {
        this.is_movie = is_movie;
    }
}
